export function defaultLang(navigatorLike = globalThis.navigator) {
  return (navigatorLike?.language || '').toLowerCase().startsWith('zh') ? 'zh' : 'en';
}

export function loadLang(storage = globalThis.localStorage) {
  const saved = storage?.getItem?.(LANG_KEY);
  return saved === 'zh' || saved === 'en' ? saved : defaultLang();
}

export function saveLang(lang, storage = globalThis.localStorage) {
  storage?.setItem?.(LANG_KEY, lang);
}

export function translate(lang, key, vars = {}) {
  const value = I18N[lang]?.[key] ?? I18N.en[key] ?? key;
  return value.replace(/\{(\w+)\}/g, (_, name) => vars[name] ?? '');
}
